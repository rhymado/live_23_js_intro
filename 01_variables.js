/*Kita bisa menggabungkan deklarasi dan assignment*/
var b = 14; // inisialisasi variabel
// console.log(b);

// const => constant => konstanta => nilai tetap
// const harus diinisialisasi dan tidak bisa direassign
const c = true;
// console.log(c);

// let
// let tidak harus diinisialisasi dan bisa direassign
let d;
// console.log(d);
d = 1;
// console.log(d);
d = false;
// console.log(d);

