// while dan do-while
let x = 5; // counter
while (x < 5) {
  console.log(x);
  x++; // perubahan counter (increment, decrement, multiplied, etc)
  // increment
  // x = x + 1
}

let y = 5; // counter
do {
  console.log(y);
  y++;
} while (y < 5);

